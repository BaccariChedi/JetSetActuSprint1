   public void  verifcation()
    {
        if(NomBox.getText().equals(""))
        {
            EtNom.setVisible(true);
        }
        if(InformatinBox.getText().equals(""))
        {
            EtInfo.setVisible(true);
        }
        if(LienMapBox.getText().equals(""))
        {
            EtMap.setVisible(true);
        }
        if(LienImageBox.getText().equals("Clique Ici"))
        {
            EtImage.setVisible(true);
        }if(TelephoneBox.getText().equals(""))
        {
            EtTel.setVisible(true);
        }
    }
