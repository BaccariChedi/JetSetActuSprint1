    private void LienImageBoxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_LienImageBoxMouseClicked
        String sh = JOptionPane.showInputDialog(null, "Veuillez saisire un Titre D'Image", "Nom Image", JOptionPane.QUESTION_MESSAGE);
        JFileChooser ch = new JFileChooser();
        ch.showOpenDialog(null);
        File f = ch.getSelectedFile();
        filename = f.getAbsolutePath();
        LienImageBox.setText(filename);
        i.setTitre(sh);
        i.setLienImage(filename);
        I.insertImage(i);
    }//GEN-LAST:event_LienImageBoxMouseClicked

