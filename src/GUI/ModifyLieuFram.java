    private void ModifiyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModifiyButtonActionPerformed
        Lieu l = new Lieu();
        LieuDAO Lr = new LieuDAO();

        try {
           VisibleFalse();
            verifcation();
            l.setNom(NomBox.getText());
            l.setInformation(InformatinBox.getText());
            if(LienImageBox.getText().equals(""))
            {
            i1 = I.findImageByTitreAndLien(i.getTitre(), i.getLienImage());
            l.setIdImage(i1.getIdImage());
            }
            l.setLienMap(LienMapBox.getText());
            l.setTel(Integer.parseInt(TelephoneBox.getText()));

            System.out.println(l.toString());
            Lr.updateLieu(l,Integer.parseInt( ID.getText()));
            JOptionPane.showMessageDialog(null, "Modification effectuée Avec Succès", " Modifier", 1);
            ListLieuxFrameModifierAndSupprimer a = new ListLieuxFrameModifierAndSupprimer();
            a.setVisible(true);
            this.dispose();
        } catch (Exception ex) {

            JOptionPane.showMessageDialog(null, "Erreur lors de la Modification", "Erreur Modifier", JOptionPane.ERROR_MESSAGE);
            System.out.println(ex.toString());
        }
    }//GEN-LAST:event_ModifiyButtonActionPerformed

