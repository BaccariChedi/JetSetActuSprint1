    private void chercherMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_chercherMouseReleased
        // TODO add your handling code here:
       
        if(inputChoix.getText().equals(""))
        {
            
        JOptionPane.showMessageDialog(this,  "Champ vide", "Gestion demandes",2);
        }
                   if(choix.getSelectedIndex()==2)
        {
            inputChoix.setText("Tout les clients");
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     tm.getDataVector().removeAllElements();  
            chargerTtClient();
        }
       else if(choix.getSelectedIndex()==0)
        {
         
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     tm.getDataVector().removeAllElements();  
            chargerClientId(Integer.parseInt(inputChoix.getText()));
        }
        else if(choix.getSelectedIndex()==1)
        {
            
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     tm.getDataVector().removeAllElements();  
            chargerNomPren(inputChoix.getText().toString());
        }
       
        
    }//GEN-LAST:event_chercherMouseReleased

