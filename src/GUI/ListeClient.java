 public void charger()
 {
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     UserDAO dao =new UserDAO();
     List<User> lis=new ArrayList<User>();
     lis=dao.DisplayAllUserNonValide();
     for(int i=0;i<lis.size();i++)
     {
         tm.addRow(new Object[]{lis.get(i).getIdUser(),lis.get(i).getNom(),lis.get(i).getPrenom(),lis.get(i).getDateNaissance()});
     }
     
     
 }
  public void chargerClientId(int id)
 {
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     UserDAO dao =new UserDAO();
    User us=new User();
     us=dao.findUserById(id);
         tm.addRow(new Object[]{us.getIdUser(),us.getNom(),us.getPrenom(),us.getDateNaissance()});
     
     
     
 }
    public void chargerNomPren(String nomprenom)
 {
     DefaultTableModel tm =(DefaultTableModel) jTable1.getModel();
     UserDAO dao =new UserDAO();
     List<User> lis=new ArrayList<User>();
     lis=dao.DisplayAllUserByNomPrenom(nomprenom);
     for(int i=0;i<lis.size();i++)
     {
         tm.addRow(new Object[]{lis.get(i).getIdUser(),lis.get(i).getNom(),lis.get(i).getPrenom(),lis.get(i).getDateNaissance()});
     }
     
     
 }
